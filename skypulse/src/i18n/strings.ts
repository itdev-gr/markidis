export type Lang = keyof typeof strings;
export const pick = <L extends Lang>(lang: L) => strings[lang];

/* Build a locale-aware URL. Pass a path like "/work" or "/" and get back
   "/work" for el and "/en/work" for en. "/" returns "/" or "/en". */
export function localeUrl(lang: Lang, path: string): string {
  const clean = path === "" ? "/" : path;
  if (lang === "en") {
    return clean === "/" ? "/en" : `/en${clean}`;
  }
  return clean;
}
